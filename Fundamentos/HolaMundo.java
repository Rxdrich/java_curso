package Fundamentos;

public class HolaMundo {
    public static void main(String args[]) {
        //Definimos la variable
         int miVarEntera = 10;
         System.out.println(miVarEntera);

         //Modificamos el valor de la variable;
         miVarEntera = 7;
         System.out.println(miVarEntera);

         String miVarCadena = "Saludos";
         System.out.println(miVarCadena);
         
         miVarCadena = "Adios";
         System.out.println(miVarCadena);
    }
}